/**
 * 주소 문자열에서 시군구코드 추출
 * @param {string} address - 도로명 또는 지번 주소
 * @returns {{ sigunguCd: string, bjdongCd: string, bun: string, ji: string } | null}
 */
function parseAddressToCode(address) {
  if (!address) return null;

  // 시군구코드 매칭 (긴 키부터 매칭 — "수원시 장안구"가 "수원시"보다 먼저)
  const sortedKeys = Object.keys(SIGUNGU_MAP).sort((a, b) => b.length - a.length);
  let sigunguCd = null;

  for (const key of sortedKeys) {
    if (address.includes(key)) {
      sigunguCd = SIGUNGU_MAP[key];
      break;
    }
  }

  // 세종시 특수처리 (구가 없음)
  if (!sigunguCd && address.includes('세종')) {
    sigunguCd = '36110';
  }

  if (!sigunguCd) return null;

  // 도로명 주소("로", "길" 포함)에서는 번/지 추출 안 함
  // 지번 주소("동", "리", "가" + 숫자 패턴)에서만 번/지 추출
  let bun = '';
  let ji = '';
  const isRoadAddr = /[로길]\s*\d/.test(address);
  if (!isRoadAddr) {
    // 지번주소: "동인동1가 30-1번지", "역삼동 123-4" 등
    const bunjiMatch = address.match(/(\d+)[-의](\d+)번?지?\s*$/) || address.match(/\s(\d+)번?지?\s*$/);
    if (bunjiMatch) {
      bun = (bunjiMatch[1] || '').padStart(4, '0');
      ji = (bunjiMatch[2] || '0').padStart(4, '0');
    }
  }

  return { sigunguCd, bjdongCd: '', bun, ji };
}

module.exports = { parseAddressToCode, SIGUNGU_MAP };
